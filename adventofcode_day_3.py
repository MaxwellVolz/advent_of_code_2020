# Right 1, down 2.

def traverse_slope(x_move, y_move):
  trees_in_path = 0
  x_pos = y_pos = 0
  vertical_steps = int(lane_height / y_move)

  for _ in range(vertical_steps):

    # print(f'{mountain[y_pos][x_pos]} at [{x_pos},{y_pos}]')

    if mountain[y_pos][x_pos] == '#':
      trees_in_path += 1

    x_pos += x_move
    y_pos += y_move

    if x_pos >= lane_width-1:
      x_pos -= lane_width

  return trees_in_path

